package model;

public class GradeStudentBean {
	private int schoolId;				// 學校編號
	private int anniversary;			// 學年度(ex:104)
	private int elementaryFirst;		// 年級(一年級)
	private int elementarySecond;		// 年級(二年級)
	private int elementaryThird;		// 年級(三年級)
	private int elementaryFourth;		// 年級(四年級)
	private int elementaryFifth;		// 年級(五年級)
	private int elementarySixth;		// 年級(六年級)
	private int juniorFirst;			// 年級(七年級)
	private int juniorSecond;			// 年級(八年級)
	private int juniorThird;			// 年級(九年級)
	public int getSchoolId() {
		return schoolId;
	}
	public void setSchoolId(int schoolId) {
		this.schoolId = schoolId;
	}
	public int getAnniversary() {
		return anniversary;
	}
	public void setAnniversary(int anniversary) {
		this.anniversary = anniversary;
	}
	public int getElementaryFirst() {
		return elementaryFirst;
	}
	public void setElementaryFirst(int elementaryFirst) {
		this.elementaryFirst = elementaryFirst;
	}
	public int getElementarySecond() {
		return elementarySecond;
	}
	public void setElementarySecond(int elementarySecond) {
		this.elementarySecond = elementarySecond;
	}
	public int getElementaryThird() {
		return elementaryThird;
	}
	public void setElementaryThird(int elementaryThird) {
		this.elementaryThird = elementaryThird;
	}
	public int getElementaryFourth() {
		return elementaryFourth;
	}
	public void setElementaryFourth(int elementaryFourth) {
		this.elementaryFourth = elementaryFourth;
	}
	public int getElementaryFifth() {
		return elementaryFifth;
	}
	public void setElementaryFifth(int elementaryFifth) {
		this.elementaryFifth = elementaryFifth;
	}
	public int getElementarySixth() {
		return elementarySixth;
	}
	public void setElementarySixth(int elementarySixth) {
		this.elementarySixth = elementarySixth;
	}
	public int getJuniorFirst() {
		return juniorFirst;
	}
	public void setJuniorFirst(int juniorFirst) {
		this.juniorFirst = juniorFirst;
	}
	public int getJuniorSecond() {
		return juniorSecond;
	}
	public void setJuniorSecond(int juniorSecond) {
		this.juniorSecond = juniorSecond;
	}
	public int getJuniorThird() {
		return juniorThird;
	}
	public void setJuniorThird(int juniorThird) {
		this.juniorThird = juniorThird;
	}
	@Override
	public String toString() {
		return "GradeStudentBean [schoolId=" + schoolId + ", anniversary="
				+ anniversary + ", elementaryFirst=" + elementaryFirst
				+ ", elementarySecond=" + elementarySecond
				+ ", elementaryThird=" + elementaryThird
				+ ", elementaryFourth=" + elementaryFourth
				+ ", elementaryFifth=" + elementaryFifth + ", elementarySixth="
				+ elementarySixth + ", juniorFirst=" + juniorFirst
				+ ", juniorSecond=" + juniorSecond + ", juniorThird="
				+ juniorThird + "]";
	}
	
	
	
}
		
		
		